package edu.ycp.cs320.lab02a_nbussiere.model;

public class Numbers {
	private double uno, dos, tres, calcular;
	
	public Numbers() {
	}
	
	public void setUno(double uno) {
		this.uno = uno;
	}
	
	public double getUno() {
		return uno;
	}
	
	public void setDos(double dos) {
		this.dos = dos;
	}
	
	public double getDos() {
		return dos;
	}

	public void setTres(double tres) {
		this.tres = tres;
	}
	
	public double getTres() {
		return tres;
	}
	
	public void setCalcular(double calcular) {
		this.calcular = calcular;
	}
	
	public double getCalcular() {
		return calcular;
	}
	
}
